// CALCULATE BUTTON
$( "#calculate" ).click(function() {
	$('.loading').show();

	var ticker = $('input[name=ticker]').val();
	var startDate = $('input[name=startDate]').val();
	var endDate = $('input[name=endDate]').val();

	if (!isValidDate(startDate) || !isValidDate(endDate)) {
		$('.loading').hide();
		$( ".popup" ).show();
		$( ".popup p" ).show();
    	$( ".popup p" ).text( 'Please enter the correct date format and try again.');
	} else {
		$.ajax({
		        type: "POST",
		        url: "garch/python",
		        data: {t: ticker, s: startDate, e: endDate},
		        success: callbackFunc
		});

		function callbackFunc(response) {
			var responseMessage = response[0];
			console.log(responseMessage);
			if (response[1] == 'False') {
				$( ".popup" ).show();
				$( ".popup img" ).hide();
				$( ".popup p" ).show();
		    	$( ".popup p" ).text(responseMessage + ' Please try again with correct input.');

			} else {
				$( ".popup" ).show();
				$( ".popup img" ).attr('src', responseMessage);
				$( ".popup img" ).show();
		    	$( ".popup p" ).hide();	
			};

			$('.loading').hide();
		    
		};
	};
});

// VALIDATE DATE
function isValidDate(dateString)
{
    // First check for the pattern
    var regex_date = /^\d{4}\-\d{1,2}\-\d{1,2}$/;

    if(!regex_date.test(dateString))
    {
        return false;
    }

    // Parse the date parts to integers
    var parts   = dateString.split("-");
    var day     = parseInt(parts[2], 10);
    var month   = parseInt(parts[1], 10);
    var year    = parseInt(parts[0], 10);

    // Check the ranges of month and year
    if(year < 1000 || year > 3000 || month == 0 || month > 12)
    {
        return false;
    }

    var monthLength = [ 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31 ];

    // Adjust for leap years
    if(year % 400 == 0 || (year % 100 != 0 && year % 4 == 0))
    {
        monthLength[1] = 29;
    }

    // Check the range of the day
    return day > 0 && day <= monthLength[month - 1];
}

